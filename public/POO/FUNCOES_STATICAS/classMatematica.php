<?php
class matematica
{
    public static int $int = 4;

    public static string $nome = 'Estudando metodos staticos';

    public static function somar($x, $y, $z)
   
    {
        return $x + $y + $z;
    }

    public  static function dividir($x, $y, $z)
    {
        return $x / $y / $z;
    }

    public static function multiplicar($x, $y, $z)
    {
        return $x * $y * $z;
    }

    public static function subtrair($x, $y, $z)
    {
        return $x * $y * $z;
    }

    
}
